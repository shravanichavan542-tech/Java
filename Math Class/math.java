import java.util.*;
public class math
{
	public static void main(String args[])
	{
		int no1=19;
		int no2=89;
		System.out.println("Maximum Value="+Math.max(no1,no2)); //max value
		System.out.println("Minimum Value="+Math.min(no1,no2)); //min value
		System.out.println("Square root of 25="+Math.sqrt(25)); //returns sqaure root
		System.out.println("Cube root of 8="+Math.cbrt(8)); //returns cube root 
		System.out.println("Round Value of 8.9="+Math.round(8.9)); //rounds nearest value 
		System.out.println("Ceil value of 8.9="+Math.ceil(8.9)); //rounds up nearest value 
		System.out.println("Floor value of 8.9="+Math.floor(8.9)); //rounds down nearest value 
		System.out.println(" 2 power of 3="+Math.pow(2,3)); //returns 2 raised to 3 value
		System.out.println("Random Value="+Math.random()); //returns random value
		System.out.println("Absolute Value of 8="+Math.abs(-8)); //returns absolute value
		System.out.println("Sin s="+Math.sin(Math.PI/2)); //return sin value of radians
		System.out.println("Cos of 0="+Math.cos(0)); //return cos value of radians
		System.out.println("tan of 0="+Math.tan(0)); //return tan value of radians
	
		
		
		
		
	}
}